static const uint16_t *get_glyph_5x10(char character) {
    int index = 0;

    if (character >= FONT_5X10_FIRST_CHAR && character <= FONT_5X10_LAST_CHAR) {
        index = character - FONT_5X10_FIRST_CHAR;
    }

    if (index < 0 || index >= FONT_5X10_GLYPH_COUNT) {
        index = 0;
    }

    return FONT_5X10[index];
}

void screen_draw_char_5x10(int x, int y, char character) {
    screen_ssd1306_draw_glyph_columns(
        x,
        y,
        get_glyph_5x10(character),
        FONT_5X10_WIDTH,
        FONT_5X10_HEIGHT);
}

void screen_draw_text_5x10(int x, int y, const char *text) {
    if (text == NULL) {
        return;
    }

    while (*text != '\0') {
        screen_draw_char_5x10(x, y, *text);
        x += FONT_5X10_ADVANCE;
        text++;
    }
}

void screen_draw_text_line_5x10(int line, const char *text) {
    screen_draw_text_5x10(0, line * FONT_5X10_LINE_HEIGHT, text);
}

