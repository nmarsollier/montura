def art_to_c_array(name, rows):
    """Convert ASCII art to C bitmap array."""
    height = len(rows)
    width = len(rows[0]) if rows else 0
    bytes_per_row = (width + 7) // 8

    hex_rows = []
    for row in rows:
        # Pad row to full width
        row = row.ljust(width, '.')
        byte_vals = []
        for b in range(bytes_per_row):
            val = 0
            for bit in range(8):
                col = b * 8 + bit
                if col < width and row[col] in ('#', '@'):
                    val |= (1 << (7 - bit))  # MSB = leftmost
            byte_vals.append(f"0x{val:02X}")
        hex_rows.append("    " + ", ".join(byte_vals) + ",")

    body = "\n".join(hex_rows)

    return f"""static const uint8_t {name}_DATA[] = {{
{body}
}};

const ScreenBitmap {name} = {{
    .width = {width},
    .height = {height},
    .data = {name}_DATA,
}};
"""


